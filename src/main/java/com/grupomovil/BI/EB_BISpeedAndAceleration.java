public class EB_BISpeedAndAceleration {

    private String velocidadVehiculo;
    private String aceleracionVehiculo;
    private String idVehiculo;
    private String fechaHoraEnvioDato;

    public String getVelocidadVehiculo() {
        return velocidadVehiculo;
    }

    public void setVelocidadVehiculo(String velocidadVehiculo) {
        this.velocidadVehiculo = velocidadVehiculo;
    }

    public String getAceleracionVehiculo() {
        return aceleracionVehiculo;
    }

    public void setAceleracionVehiculo(String aceleracionVehiculo) {
        this.aceleracionVehiculo = aceleracionVehiculo;
    }

    public String getIdVehiculo() {
        return idVehiculo;
    }

    public void setIdVehiculo(String idVehiculo) {
        this.idVehiculo = idVehiculo;
    }

    public String getFechaHoraEnvioDato() {
        return fechaHoraEnvioDato;
    }

    public void setFechaHoraEnvioDato(String fechaHoraEnvioDato) {
        this.fechaHoraEnvioDato = fechaHoraEnvioDato;
    }
        
}
